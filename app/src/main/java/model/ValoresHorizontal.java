package model;

public class ValoresHorizontal {
    private String anguloTan;
    private String puntoInter;
    private String velocProy;
    private String gradoCurva;

    public String getAnguloTan() { return anguloTan; }

    public void setAnguloTan(String anguloTan) { this.anguloTan = anguloTan; }

    public String getPuntoInter() { return puntoInter; }

    public void setPuntoInter(String puntoInter) { this.puntoInter = puntoInter; }

    public String getVelocProy() { return velocProy; }

    public void setVelocProy(String velocProy) { this.velocProy = velocProy; }

    public String getGradoCurva() { return gradoCurva; }

    public void setGradoCurva(String gradoCurva) { this.gradoCurva = gradoCurva; }
}
